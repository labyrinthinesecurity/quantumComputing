def amplify(n_qubits=4):
    circ = Circuit()
    circ.h(np.arange(n_qubits))
    circ.x([0,1,2,3]).cccz(targets=[0, 1, 2,3]).x([0,1,2,3])
    circ.h(np.arange(n_qubits))
    return circ

def diffuse(n_qubits=4):
    circ = Circuit()
    circ.h(np.arange(n_qubits))
    circ.x([0,1,2,3]).h([0]).cccnot(targets=[3,1,2,0]).h([0]).x([0,1,2,3])
    circ.h(np.arange(n_qubits))
    return circ

@circuit.subroutine(register=True)
def quantum_walk():
  ocirc=Circuit()
  ocirc.ccnot(4,5,0).ccnot(5,0,2)
  return ocirc
@circuit.subroutine(register=True)
def reverse_quantum_walk():
  ocirc=Circuit()
  ocirc.ccnot(5,0,2).ccnot(4,5,0)
  return ocirc

@circuit.subroutine(register=True)
def init():
  scirc=Circuit()
  scirc.i(range(7)).x([6]).h([0,1,2,3,4,5,6])
#  print(scirc)
  return scirc

def run(n_reps):
  circ = Circuit()
  circ.init()
  for r in range(n_reps):
    qw=quantum_walk().ccccnot(targets=[0,1,2,3,6]).reverse_quantum_walk()
    circ.add(qw)
    amplification=diffuse()
    circ.add(amplification)
  if n_reps==0:
    qw=quantum_walk().ccccnot(targets=[0,1,2,3,6]).reverse_quantum_walk()
    circ.add(qw)
  task = device.run(circ, shots=nbShots)
  result = task.result()
  counts = result.measurement_counts
  new_results = {}
  for bitstring, count in result.measurement_counts.items():
      # Only keep the outcomes on first n qubits
      trunc_bitstring = bitstring[:6]
      # Add the count to that of the of truncated bit string
      new_results[trunc_bitstring] = new_results.get(trunc_bitstring, 0) + count
  return new_results

